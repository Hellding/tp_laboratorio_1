#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED
/** \brief toma dos numeros y los suma.
*
* \param 1º operando.
* \param 2º operando.
* \return suma de los dos operandos.
*
*/
float suma(float,float);
/** \brief toma dos numeros y los resta.
*
* \param 1º operando.
* \param 2º operando.
* \return resta de los dos operandos.
*
*/
float resta(float,float);
/** \brief toma dos numeros decimales y divide el 1º por el 2º.
*
* \param 1º operando.
* \param 2º operando.
* \return dividision del 1º operando por el 2º operando.
*
*/
float division(float,float);
/** \brief toma dos numeros y multiplica el 1º operando por el 2º operando.
*
* \param 1º operando.
* \param 2º operando.
* \return multiplicacion del 1º operando por el 2º operando.
*
*/
float multiplicacion(float,float);
/** \brief toma el 1º operando y calcula su factorial
*
* \param 1º operando.
* \return factorial del 1º operando.
*
*/
long factorial(float);

#endif // FUNCIONES_H_INCLUDED
